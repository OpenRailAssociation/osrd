CREATE TABLE stdcm_search_environment (
    id int8 PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    infra_id int8 NOT NULL REFERENCES infra(id), -- No CASCADE
    electrical_profile_set_id int8 REFERENCES electrical_profile_set(id), -- No CASCADE
    work_schedule_group_id int8 REFERENCES work_schedule_group(id), -- No CASCADE
    timetable_id int8 NOT NULL REFERENCES timetable_v2(id), -- No CASCADE
    search_window_begin timestamptz NOT NULL,
    search_window_end timestamptz NOT NULL
);

-- No index because this table should have very few rows, it's almost a singleton
